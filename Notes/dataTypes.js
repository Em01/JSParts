/*
ECMAScript standard defines seven data types:
6 are primitives:
* Number - Only one nuber type-double precision 64 bit binary format IEEE 754 value.
* Boolean - True or False
* String
* Null - has only one value Null
* Undefined - A variable with No Value
* Symbol (ES6)

And Object

Primitive is data that is not an object and has no methods. Usually a primitive
is represented directly at the lowest value of the language implementation.
They are immutable (cannot be changed)

Except for null and undefined, all primitive values have object equivalents that
wrap around the primitive values. These have certain methods attached.

The wrapper's valueOf() method will return the primitive value.

Languages have built-in data structures.

String-Text enclosed within quotation marks

Number-A number but with no enclosing quotes

Boolean-True or False value-They are keywords in javaScript so quotes are not
needed.

Array-A data structure that allows you to store multiple values in one single
reference

Object-Everything in JavaScript is a object and can be stored in a variable.
*/
